export function getLineNumber(idx, windowWidth) {
  let lineNumber = 0,
    ni = 0,
    widthSum = 0;
  while (ni <= idx) {
    widthSum += getWidth(`${ni}`);
    ni++;
    lineNumber = 1 + Math.floor(widthSum / windowWidth);
  }
  return lineNumber;
}

export function getWidth(divName) {
  return document.getElementById(divName).offsetWidth;
}

