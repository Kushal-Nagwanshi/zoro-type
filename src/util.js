export function isValidKey(key) {
  if (key.length > 1) return false;
  // console.log(key) ;
  // console.log("inside isValidkey") ;
  const valid_characters = [
    "!",
    "'",
    "#",
    "$",
    "%",
    "&",
    "'",
    "(",
    ")",
    "*",
    "+",
    ",",
    "-",
    ".",
    "/",
    ":",
    ";",
    "<",
    "=",
    ">",
    "?",
    "@",
    "[",
    "\\",
    "]",
    "^",
    "_",
    "`",
    "{",
    "|",
    "}",
    "~",
  ];

  if (key.match(/[a-zA-Z]/)) return true;
  else if (key.match(/[0-9]/)) return true;

  for (let i = 0; i < valid_characters.length; i++) {
    if (key === valid_characters[i]) return true;
  }
  return false;
}

export function getRandomWords(count) {
  let res = Array();
  for (let i = 0; i < count; i++) {
    let randomWord =
      Top100Words[Math.floor(Math.random() * Top100Words.length)];
    randomWord += " ";
    res.push(randomWord);
  }
  return res;
}

